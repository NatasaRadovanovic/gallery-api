     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(CommentRequest $request)
    {
        $user = auth()->user();

       $comment = Comment::create([
            'body' => $request->body,
            'gallery_id' => $request->gallery_id,
            'user_id' => $user->id,
        ]);
        
        $comment->load('user');
        return $comment;
